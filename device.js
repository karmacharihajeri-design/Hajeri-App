function getDeviceId() {
  let id = localStorage.getItem("hajeriDeviceId");
  if (!id) {
    id = (crypto.randomUUID
      ? crypto.randomUUID()
      : "dev-" + Date.now() + "-" + Math.random().toString(36).slice(2));
    localStorage.setItem("hajeriDeviceId", id);
  }
  return id;
}
